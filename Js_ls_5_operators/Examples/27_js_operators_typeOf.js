/* TypeOf */
console.log(typeof 10)       // 'number'

console.log(typeof "Paul")       // 'string'

console.log(typeof false)       // 'boolean'

console.log(typeof undefined)       // 'undefined'

/* Простые варианты применения */
console.log("------------------------------------------------------------------")
console.log(typeof 10 === 'number')       // 'true' 

